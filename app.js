module.exports = {
    //suma
    add: function(value1, value2){
        return value1 + value2;
    },
    //resta
    sub: function(value1, value2){
        return value1 - value2;
    },
    //multiplicacion
    mult: function(value1, value2){
        return value1 * value2;
    },
    //division
    div: function(value1, value2){
        return value1 / value2;
    }
}